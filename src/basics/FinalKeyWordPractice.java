package basics;

public class FinalKeyWordPractice {

	public static void main(String[] args) {
		
		// final keyword has 3 use cases
				// 1 - final variables = you can initialize the variable, but can not change the value after that
				// 2 - final methods = you can not override the method anymore
				// 3 - final class = you can not inherit from a final class
				
				final int x = 10;
				
				
			///	System.out.println(integer.MAX_VALUE);
			///	System.out.println(integer.MIN_VALUE);
				
				
				System.out.println(x);
				
	}
}
		
		